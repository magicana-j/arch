#!/usr/bin/env bash
# setup_cjk_ime.sh
# CJK fonts and Japanese IME setup for Arch Linux
# Target: Panasonic CF-SZ6 / KDE Plasma 6 (Wayland) / Arch Linux 2026-02

set -euo pipefail

# ---------------------------------------------------------------------------
# Fonts
#   Japanese      : ipaexfont-otf      (IPA ex fonts)
#   Simplified CN : wqy-zenhei         (official repo, covers GB2312/CJK unified)
#   Traditional CN: ttf-hannom         (official repo, covers CNS/traditional CJK)
#   Korean        : ttf-baekmuk        (official repo)
#   Monospace     : ttf-jetbrains-mono (official extra repo)
# ---------------------------------------------------------------------------
FONT_PACKAGES=(
    ipaexfont-otf
    wqy-zenhei
    ttf-hannom
    ttf-baekmuk
    ttf-jetbrains-mono
)

# ---------------------------------------------------------------------------
# IME: fcitx5 + Mozc
# ---------------------------------------------------------------------------
IME_PACKAGES=(
    fcitx5
    fcitx5-mozc
    fcitx5-configtool
    fcitx5-qt
    fcitx5-gtk
)

# ---------------------------------------------------------------------------
# Environment variable template destination
#
# ~/.config/environment.d/fcitx5.conf is read by the systemd user session
# (supported since Plasma 5.22+) and is the recommended approach for Wayland.
# This avoids writing to system-wide /etc/environment.
# ---------------------------------------------------------------------------

# ---------------------------------------------------------------------------
# Helper functions
# ---------------------------------------------------------------------------
info()    { echo "[INFO]  $*"; }
success() { echo "[OK]    $*"; }
warn()    { echo "[WARN]  $*"; }
err()     { echo "[ERROR] $*" >&2; }

check_root() {
    if [[ $EUID -ne 0 ]]; then
        err "Run this script as root (or via sudo)."
        exit 1
    fi
}

install_packages() {
    local -n pkgs=$1
    info "Installing: ${pkgs[*]}"
    pacman -Sy --noconfirm --needed "${pkgs[@]}"
}

place_env_template() {
    # Resolve the real home dir even when running under sudo
    local real_user="${SUDO_USER:-${USER}}"
    local real_home
    real_home="$(getent passwd "${real_user}" | cut -d: -f6)"
    local dest="${real_home}/.config/environment.d/fcitx5.conf"
    local dir
    dir="$(dirname "${dest}")"

    mkdir -p "${dir}"

    if [[ -f "${dest}" ]]; then
        warn "Template already exists at ${dest} -- skipping (no overwrite)."
        return
    fi

    cat > "${dest}" << 'EOF'
# fcitx5 environment variables for KDE Plasma 6 (Wayland)
# Generated by setup_cjk_ime.sh
#
# Loaded by systemd user session via environment.d(5).
# Apply without rebooting: systemctl --user daemon-reexec, then re-login.
#
# --- Active ---

# XWayland / legacy X11 apps (XIM protocol)
XMODIFIERS=@im=fcitx

# GTK3 apps that don't implement Wayland text-input protocol natively
GTK_IM_MODULE=fcitx

# Qt5 apps (Qt5 falls back to XIM on Wayland without this)
# Qt6 uses the native Wayland text-input protocol via fcitx5-qt; not needed.
QT_IM_MODULE=fcitx

# --- Optional (uncomment if needed) ---

# SDL2-based applications
#SDL_IM_MODULE=fcitx

# kitty terminal (GLFW-based)
#GLFW_IM_MODULE=ibus
EOF

    chown -R "${real_user}:" "${dir}"
    success "Environment template placed at: ${dest}"
}

# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------
main() {
    check_root

    info "=== Step 1: Install CJK fonts ==="
    install_packages FONT_PACKAGES
    success "CJK fonts installed."

    info "=== Step 2: Update font cache ==="
    fc-cache -fv > /dev/null
    success "Font cache updated."

    info "=== Step 3: Install fcitx5-mozc and related packages ==="
    install_packages IME_PACKAGES
    success "fcitx5-mozc installed."

    info "=== Step 4: Place fcitx5 environment variable template ==="
    place_env_template

    echo ""
    echo "================================================================"
    echo "  Setup complete."
    echo "  NEXT STEPS (manual):"
    echo "    1. Register fcitx5 as Virtual Keyboard in KDE:"
    echo "       System Settings > Input & Output > Keyboard"
    echo "       > Virtual Keyboard > select 'Fcitx 5'"
    echo "    2. Configure input methods via fcitx5-configtool."
    echo "    3. Reboot to apply environment variables."
    echo "================================================================"
}

main "$@"
